// relational operator
/* 
  ==,>,< ,!= ,>=,<=
*/

#include<stdio.h>

void main(){

   int a = 20 , b = 10;

   printf("Greater than            : %d",a>b);
   printf("\nless than             : %d",a<b);
   printf("\nGreater than or equal : %d",a>=b);
   printf("\nless than or equal    : %d",a<=b);
   printf("\nboth are equal        : %d",a==b);

}